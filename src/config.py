"""
Центральные константы пайплайна.

  - DPI рендеринга для YOLO- и PDF-слоёв
  - Стандартные директории
  - Размеры изображений для OCR и итогового PNG
  - Эвристики очистки текста и анти-галлюцинаций
  - Пороги матчинга Docling-item и YOLO-block

Модули пайплайна импортируют из этого файла, чтобы не дублировать магические
числа.
"""

from __future__ import annotations

# ---------------------------------------------------------------------------
# DPI
# ---------------------------------------------------------------------------

# YOLO-растр 400 DPI для высокой чёткости мелкого текста и аккуратных bbox.
LAYOUT_DPI = 400
PDF_DPI = 72

# Производные коэффициенты. Используются в coord_projection и layout_router.
PDF_TO_LAYOUT = PDF_DPI / LAYOUT_DPI   # points * PDF_TO_LAYOUT -> pixels (inv)
LAYOUT_TO_PDF = LAYOUT_DPI / PDF_DPI   # points * LAYOUT_TO_PDF -> pixels (fwd)


# ---------------------------------------------------------------------------
# Директории
# ---------------------------------------------------------------------------

OUTPUT_DIR = "data/output"
RAW_DIR = "data/raw"
WEIGHTS_DIR = "weights"


# ---------------------------------------------------------------------------
# Размеры изображений
# ---------------------------------------------------------------------------

# Длинная сторона кропа для olmOCR.
OLM_RENDER_SIDE = 1288
# Размер сжатого PNG в submission.
IMAGE_MAX_SIDE = 150
# Отступ в PDF points.
CROP_PAD_PTS = 2.0


# ---------------------------------------------------------------------------
# Эвристики очистки текста и анти-галлюцинаций
# ---------------------------------------------------------------------------

# Минимальное количество символом для валидных строк.
MIN_VALID_CHARS = 3
# Минимальное количество строк для вычисления самой частой непустой строки (признак цикла VLM).
MIN_LINES_REP_CHECK = 4


# ---------------------------------------------------------------------------
# Пороги матчинга Docling-item и YOLO-block
# ---------------------------------------------------------------------------

# Минимальный IoM для сопоставления блоков.
IOM_MATCH_THRESHOLD = 0.6
# Docling-item не должен быть > MAX_AREA_RATIO * YOLO-блока.
MAX_AREA_RATIO = 8.0
